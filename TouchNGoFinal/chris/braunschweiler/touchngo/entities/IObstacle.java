package chris.braunschweiler.touchngo.entities;

/**
 * An obstacle such as a wall, water or anything else that can obstruct a player's path.
 * @author chrisbraunschweiler
 *
 */
public interface IObstacle extends IEntity{
	
	/**
	 * The type of obstacle (water, wall etc).
	 * @author chrisbraunschweiler
	 *
	 */
	public enum ObstacleType{
		WALL,
		WATER
	}
	
	/**
	 * Gets the type of the obstacle (ie: Wall, water).
	 * @return
	 */
	ObstacleType getType();
}
