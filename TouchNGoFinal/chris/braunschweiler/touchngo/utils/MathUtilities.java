package chris.braunschweiler.touchngo.utils;

/**
 * Utilities class that contains simple math methods.
 * @author chrisbraunschweiler
 *
 */
public class MathUtilities {
	
	/**
	 * Calculates the distance between the given source and destination.
	 * @param sourceX X component of the source position.
	 * @param sourceY Y component of the source position.
	 * @param destinationX X component of the destination position.
	 * @param destinationY Y component of the destination position.
	 * @return The distance between the given source and destination positions.
	 */
	public static int calculateDistanceBetween(int sourceX, int sourceY, int destinationX, int destinationY){
		int distance = (int)Math.sqrt((Math.pow(Math.abs(sourceX - destinationX), 2) + Math.pow(Math.abs(sourceY - destinationY), 2)));
		return distance;
	}
}
